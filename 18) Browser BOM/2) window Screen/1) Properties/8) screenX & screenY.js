//! window.screenX and window.screenY properties to find the position of the current window relative to the screen.
//? If the window is moved to another monitor, these properties can help you determine which screen it's on.

console.log("ScreenX position of the window:", window.screenX);  // Horizontal distance from the left edge of the primary monitor
console.log("ScreenY position of the window:", window.screenY);  // Vertical distance from the top edge of the primary monitor
